#include <iostream>

int main()
{
    int first, second;
    std::cout << "Enter an integer integer: ";
    std::cin >> first;
    std::cout << "Enter another integer: ";
    std::cin >> second;
    std::cout << first << " + " << second << " is " << first + second << "." << std::endl;
    std::cout << first << " - " << second << " is " << "." << first - second;
}
